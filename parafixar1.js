// Adicione um valor ao saldo
let saldo = 3500;
function adicionaUmValor (valor){
  return saldo + valor
}

//Subtraia um valor do saldo.
function subtraiUmValor (valor){
  return saldo - valor
}

//Multiplique um valor do saldo.
function multipliqueUmValor (valor){
  return saldo * valor 
}

//Divida um valor do saldo.
function dividaUmValor (valor){
  return saldo / valor
}

console.log(adicionaUmValor(500));
console.log(subtraiUmValor(500));
console.log(multipliqueUmValor(2));
console.log(dividaUmValor(2));